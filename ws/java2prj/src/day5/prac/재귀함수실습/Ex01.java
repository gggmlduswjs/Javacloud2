package day5.prac.재귀함수실습;

public class Ex01 {
	
	
	public static void main(String[] args) {
		
		// 1~5까지의 합
			// 순번	 1,2,3,4,5
			// 수(항) 1,2,3,4,5
		
				// i => 두 가지 역할(반복, 항을 만들어내는 변수)
			
			int sum=0;	// 출력변수
			for(int i=1;i<=5;i++) {
				sum = sum+i;
			}
		
			System.out.println(sum);
		// 매서드
		
	}
	
	

}
